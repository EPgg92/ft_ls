#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	s1_len;

	s1_len = ft_strlen(s1);
	while (*s2 && n)
	{
		s1[s1_len++] = *s2++;
		n--;
	}
	s1[s1_len] = '\0';
	return (s1);
}
