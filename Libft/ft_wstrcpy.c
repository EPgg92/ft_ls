#include "libft.h"

wchar_t		*ft_wstrcpy(wchar_t *s1, wchar_t *s2)
{
	int index;

	index = 0;
	while (*s2)
		s1[index++] = *s2++;
	s1[index] = 0;
	return (s1);
}
