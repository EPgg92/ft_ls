#include "libft.h"

size_t		determine_bytes(wchar_t c)
{
	int bytes;
	int pow;

	bytes = 1;
	pow = 7;
	while (c >= ft_pow(2, pow))
	{
		bytes++;
		pow += pow != 11 ? 4 : 5;
	}
	return (bytes);
}
